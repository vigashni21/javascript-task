var message;

message = "Hello World!";
console.log(message);

message = 'Congratulations! Your tweet has won a prize...';
console.log(message);

// Assigning third value to message
message = "Thank You!";
console.log(message);

// Spliting the first message into two pieces
message = "Hello " + "World!";
console.log(message)

// Spliting the second message
message = 'Congratulations' + '! Your' + ' tweet has won a prize...';
console.log(message);